    <h2 class="mt-8">
      Bullying as a result of any form of discrimination
    </h2>
    <p class="mb-4">
      Bullying because of discrimination occurs when bullying is motivated by a prejudice against certain people or groups of people. This may be because of their gender, age, race, nationality, ethnic origin, religion or belief, sexual orientation, gender reassignment, disability or ability.
    </p>
    <p class="mb-4">
      Generally, these forms of bullying look like other sorts of bullying, but in particular it can include:
    </p>
    <ul
      class="list-disc pl-5"
      role="list"
    >
      <li>
        <p>Verbal abuse – derogatory remarks about girls or women, suggesting girls and women are inferior to boys and men, or that black, Asian and ethnic minority people are not as capable as white people; spreading rumours that someone is gay, suggesting that something or someone is inferior and so they are “gay” – for example, “you’re such a gay boy!” or “those trainers are so gay!”. Ridiculing someone because of a disability or mental health related issue, or because they have a physical, mental or emotional developmental delay. Referring to someone by the colour of their skin, rather than their name; using nicknames that have racial connotations; isolating someone because they come from another country or social background etc.</p>
      </li>
      <li>
        <p>Physical abuse – including hitting, punching, kicking, sexual assault, and threatening behaviour.</p>
      </li>
      <li>
        <p>Cyberbullying – using online spaces to spread rumours about someone or exclude them. It can also include text messaging, including video and picture messaging.</p>
      </li>
    </ul>

