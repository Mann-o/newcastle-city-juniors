        <p>feels ill before training sessions</p>
      </li>
      <li>
        <p>comes home with clothes torn or training equipment damaged</p>
      </li>
      <li>
        <p>has possessions which have ‘gone missing</p>
      </li>
      <li>
        <p>asks for money or starts stealing money (to pay the bully)</p>
      </li>
      <li>
        <p>has unexplained cuts or bruises</p>
      </li>
      <li>
        <p>is frightened to say what’s wrong</p>
      </li>
      <li>
        <p>gives improbable excuses for any of the above.</p>
      </li>
    </ul>
    <p class="mb-4">
      In more extreme cases, if a child:
    </p>
    <ul
      class="mb-4 list-disc pl-5"
