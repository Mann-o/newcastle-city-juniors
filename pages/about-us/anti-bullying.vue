    <h1>Anti-Bullying Policy</h1>
    <p>Newcastle City Juniors are committed to providing a caring, friendly, and safe environment for all of our members so they can participate in football in a relaxed and secure atmosphere. Bullying of any kind is unacceptable at our club. If bullying does occur, all club members or parents should be able to tell and know that incidents will be dealt with promptly and effectively. We are a <em>TELLING</em> club. This means that anyone who knows that bullying is happening is expected to tell the Club Welfare Officer or any committee member. This club is committed to playing its part to teach players to treat each other with respect.</p>

    <h2 class="mt-8">
      What is Bullying?
    </h2>
    <p class="mb-4">
      Bullying is the use of aggression with the intention of hurting another person. Bullying results in pain and distress to the victim.
    </p>
    <p class="mb-4">
      Examples of bullying:
    </p>
    <ul
      class="list-disc pl-5"
      role="list"
    >
      <li>
        <p>Emotional being unfriendly, excluding (emotionally and physically) sending hurtful text messages, tormenting, (e.g. hiding football boots/shin guards, threatening gestures).</p>
      </li>
      <li>
        <p>Physical pushing, kicking, hitting, punching or any use of violence.</p>
      </li>
      <li>
        <p>Sexual unwanted physical contact or sexually abusive comments.</p>
      </li>
      <li>
        <p>Discrimination racial taunts, graffiti, gestures, homophobic comments, jokes about disabled people, sexist comments.</p>
      </li>
      <li>
        <p>Verbal name-calling, sarcasm, spreading rumours, teasing.</p>
      </li>
    </ul>

    <h2 class="mt-8">
      Cyberbullying
    </h2>
    <p class="mb-4">
      This is when a person uses technology i.e. mobile phones or the internet (social networking sites, chat rooms, instant messenger, tweets), to deliberately upset someone. Bullies often feel anonymous and ‘distanced’ from the incident when it takes place online and ‘bystanders’ can easily become bullies themselves by forwarding the information on. There is a growing trend for bullying to occur online or via texts – bullies no longer rely on being physically near to the young person.
    </p>
    <p>Newcastle City Juniors commits to ensure our website(s) and/or social networking pages are being used appropriately and any online bullying will be dealt with swiftly and appropriately in line with procedures detailed in this policy.</p>

